package Unit14;

import java.util.*;

public class whilePractice 
{
    public static void main(String[] args) 
    {
        int i = 0;
        while (i <= 25) {
            System.out.print(i + " ");
            i++;
        }
        i = 50;
        while (i <= 70) {
            System.out.print(i + " ");
            i++;
        }
        i = 0;
        System.out.println("\n");
        while (i <= 100) {
            System.out.print(i + " ");
            i+=5;
        }
        i = 20;
        System.out.println("\n");
        while (i > 0) {
            System.out.print(i + " ");
            i--;
        }
        i = 0;
        System.out.println("\n");
        
        while (i <= 15) {
            System.out.print("computer ");
            i++;
        }
    }
}

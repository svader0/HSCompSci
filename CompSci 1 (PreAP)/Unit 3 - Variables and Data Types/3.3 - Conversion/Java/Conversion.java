package Unit3;



public class Conversion 
{
    public static void main(String[] args) 
    {
        double inch = 2.54;
        double yard = 0.9144;
        double mile = 1.609;
        double centimeter = 0.3937;
        double meter = 1.094;
        double kilometer = 0.6214;
        double lightYear = 9.464E12;
        
        System.out.println("1 inch = " + inch);
        System.out.println("1 yard = " + yard);
        System.out.println("1 mile = " + mile);
        System.out.println("1 centimeter = " + centimeter);
        System.out.println("1 meter = " + meter);
        System.out.println("1 kilometer = " + kilometer);
        System.out.println("1 light year = " + lightYear);        
        
    }
}

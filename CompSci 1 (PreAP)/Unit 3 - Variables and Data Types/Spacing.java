package Unit3;

import java.util.*;

public class Spacing 
{
    public static void main(String[] args) 
    {
           System.out.println(5 + " + " + 10 + " = " + (5+10));
           System.out.println(30 + " / " + 5 + " = " + (30/5));
           System.out.println(50 + " + " + 20 + " - " + 6 + " * " + 3 + " = " + (50 + 20 - 6*3));
           System.out.println(40 + " / " + "(3 + 5)" + " * 2 = " + (40/(3+5)*2) + "\n");
           System.out.println("The remainder when you divide 15 by 4 is " + (15 % 4) + "\n");
           System.out.println("Jack had 3 pencils and Jill had 5. \n Jack and Jill had " + (3 + 5) + " pencils.");
           
           
    }
}
